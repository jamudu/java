    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean ok = comprobarCampos();
        String msg = "";
        if (ok) {
            if (modo.equals("alta")) {
                if (Seleccion.todosEntrenadores.existeEntrenador(entrenador)) {
                    JOptionPane.showMessageDialog(this, "Yaexiste un entrenador con ese nombre", "ERROR: entrenador duplicado", JOptionPane.ERROR_MESSAGE);
                    ok = false;
                } else {
                    msg = "Entrenador dado de alta";
                    Seleccion.todosEntrenadores.altaEntrenador(entrenador);
                }
            }
        } else {
            msg = "Entrenador modificado";
        }
        if (ok) {
            Seleccion.ficheroEntrenadores.grabar(Seleccion.todosEntrenadores);
            JOptionPane.showMessageDialog(this, msg);
            dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    private boolean comprobarCampos() {
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se pueden dejar campos en blanco", "ERROR: campos en blanco", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Debes indicar un pais", "ERROR: nacionalidad incorrecta", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jXDatePicker1.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Debes indicar una fecha de nacimiento", "ERROR: fecha incorrecta", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

